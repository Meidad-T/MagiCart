export async function importHebStores() {
  console.log('Starting H-E-B store import...');
  
  // Parse the data
  const lines = hebStoreData.trim().split('\n');
  const stores = lines.map(line => {
    const parts = line.split('\t');
    if (parts.length >= 5) {
      const storeRegion = parts[0]?.trim() || '';
      const address = parts[1]?.trim() || '';
      const city = parts[2]?.trim() || '';
      const state = parts[3]?.trim() || '';
      const zip = parts[4]?.trim() || '';

      // Skip entries with missing address
      if (!address || address === '' || zip === '0-0') {
        return null;
      }

      return {
        chain: 'HEB',
        name: `H-E-B ${city}`,
        address_line1: address,
        city: city,
        state: state,
        zip_code: zip,
        division_number: storeRegion,
        latitude: null,
        longitude: null,
        geolocation: null,
        hours: null,
        departments: null
      };
    }
    return null;
  }).filter(store => store !== null);

  console.log(`Parsed ${stores.length} H-E-B stores`);

  // Insert in batches to avoid hitting size limits
  const batchSize = 100;
  let totalInserted = 0;

  for (let i = 0; i < stores.length; i += batchSize) {
    const batch = stores.slice(i, i + batchSize);
    
    try {
      const { data, error } = await supabase
        .from('store_locations')
        .insert(batch);

      if (error) {
        console.error(`Error inserting batch ${Math.floor(i / batchSize) + 1}:`, error);
        throw error;
      }

      totalInserted += batch.length;
      console.log(`Inserted batch ${Math.floor(i / batchSize) + 1}: ${batch.length} stores (Total: ${totalInserted})`);
    } catch (error) {
      console.error(`Failed to insert batch starting at index ${i}:`, error);
      throw error;
    }
  }

  console.log(`Successfully imported ${totalInserted} H-E-B stores!`);
  return { success: true, count: totalInserted };
}
