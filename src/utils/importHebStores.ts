export async function importHebStores() {
  console.log('Starting H-E-B store import from CSV...');
  
  // Parse CSV data
  const lines = hebCsvData.trim().split('\n');
  const headers = lines[0].split(',');
  
  console.log('CSV Headers:', headers);
  
  const stores = lines.slice(1).map((line, index) => {
    // Handle CSV parsing with quoted fields
    const fields = [];
    let currentField = '';
    let inQuotes = false;
    
    for (let i = 0; i < line.length; i++) {
      const char = line[i];
      if (char === '"') {
        inQuotes = !inQuotes;
      } else if (char === ',' && !inQuotes) {
        fields.push(currentField.trim());
        currentField = '';
      } else {
        currentField += char;
      }
    }
    fields.push(currentField.trim()); // Don't forget the last field
    
    // Skip invalid entries
    if (fields.length < 8) {
      console.log(`Skipping line ${index + 2}: insufficient fields`, fields);
      return null;
    }
    
    const storeFormat = fields[0]?.trim() || '';
    const storeRegion = fields[1]?.trim() || '';
    const storeNumber = fields[2]?.trim() || '';
    const storeName = fields[3]?.trim() || '';
    const address = fields[4]?.trim() || '';
    const city = fields[5]?.trim() || '';
    const state = fields[6]?.trim() || '';
    const zip = fields[7]?.replace(/['"]/g, '').trim() || '';
    const phone = fields[8]?.replace(/['"]/g, '').trim() || '';

    // Skip entries with missing critical data
    if (!address || !city || !zip || zip === '0-0' || address === '') {
      console.log(`Skipping ${storeName}: missing address data`);
      return null;
    }

    return {
      chain: 'HEB',
      name: storeName || `H-E-B ${city}`,
      store_number: storeNumber,
      division_number: storeRegion,
      address_line1: address,
      city: city,
      state: state,
      zip_code: zip,
      phone: phone || null,
      latitude: null,
      longitude: null,
      geolocation: null,
      hours: null,
      departments: storeFormat ? { format: storeFormat } : null
    };
  }).filter(store => store !== null);

  console.log(`Parsed ${stores.length} valid H-E-B stores from CSV`);

  // Insert in batches to avoid hitting size limits
  const batchSize = 100;
  let totalInserted = 0;

  for (let i = 0; i < stores.length; i += batchSize) {
    const batch = stores.slice(i, i + batchSize);
    
    try {
      const { data, error } = await supabase
        .from('store_locations')
        .insert(batch);

      if (error) {
        console.error(`Error inserting batch ${Math.floor(i / batchSize) + 1}:`, error);
        throw error;
      }

      totalInserted += batch.length;
      console.log(`Inserted batch ${Math.floor(i / batchSize) + 1}: ${batch.length} stores (Total: ${totalInserted})`);
    } catch (error) {
      console.error(`Failed to insert batch starting at index ${i}:`, error);
      throw error;
    }
  }

  console.log(`Successfully imported ${totalInserted} H-E-B stores!`);
  return { success: true, count: totalInserted };
}
